int daysUntil(DateTime? target) {
  if (target == null) return 0;

  final nowUtc = DateTime.now().toUtc();
  final targetUtc = target.toUtc();

  final diff = targetUtc.difference(nowUtc);
  if (diff.inSeconds <= 0) return 0;

  final fullDays = diff.inDays;
  final hasRemainder = diff - Duration(days: fullDays) > Duration.zero;
  return fullDays + (hasRemainder ? 1 : 0);
}
